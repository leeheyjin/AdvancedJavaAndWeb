package kr.or.ddit.report.vo;

public class ReportVO {
	private int rep_no    ;
	private String rep_reason;
	private String rep_dtl   ;
	private String rep_action;
	private int comm_no   ;
	private int mt_no   ;
	private String mem_id    ;
	
	public int getRep_no() {
		return rep_no;
	}
	public void setRep_no(int rep_no) {
		this.rep_no = rep_no;
	}
	public String getRep_reason() {
		return rep_reason;
	}
	public void setRep_reason(String rep_reason) {
		this.rep_reason = rep_reason;
	}
	public String getRep_dtl() {
		return rep_dtl;
	}
	public void setRep_dtl(String rep_dtl) {
		this.rep_dtl = rep_dtl;
	}
	public String getRep_action() {
		return rep_action;
	}
	public void setRep_action(String rep_action) {
		this.rep_action = rep_action;
	}
	public int getComm_no() {
		return comm_no;
	}
	public void setComm_no(int comm_no) {
		this.comm_no = comm_no;
	}
	public int getMt_no() {
		return mt_no;
	}
	public void setMt_no(int mt_no) {
		this.mt_no = mt_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	
	
}                          
