package kr.or.ddit.community.vo;

import java.util.Date;

public class CommunityVO {
	private int comm_no        ;
	private String mem_id         ;
	private String comm_ttl       ;
	private String comm_type      ;
	private String comm_dtl       ;
	private String comm_del       ;
	private int comm_hits      ;
	private Date comm_date;
	private String comm_file      ;
	
	public int getComm_no() {
		return comm_no;
	}
	public void setComm_no(int comm_no) {
		this.comm_no = comm_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getComm_ttl() {
		return comm_ttl;
	}
	public void setComm_ttl(String comm_ttl) {
		this.comm_ttl = comm_ttl;
	}
	public String getComm_type() {
		return comm_type;
	}
	public void setComm_type(String comm_type) {
		this.comm_type = comm_type;
	}
	public String getComm_dtl() {
		return comm_dtl;
	}
	public void setComm_dtl(String comm_dtl) {
		this.comm_dtl = comm_dtl;
	}
	public String getComm_del() {
		return comm_del;
	}
	public void setComm_del(String comm_del) {
		this.comm_del = comm_del;
	}
	public int getComm_hits() {
		return comm_hits;
	}
	public void setComm_hits(int comm_hits) {
		this.comm_hits = comm_hits;
	}
	public Date getComm_date() {
		return comm_date;
	}
	public void setComm_date(Date comm_date) {
		this.comm_date = comm_date;
	}
	public String getComm_file() {
		return comm_file;
	}
	public void setComm_file(String comm_file) {
		this.comm_file = comm_file;
	}
	@Override
	public String toString() {
		return "CommunityVO [comm_no=" + comm_no + ", mem_id=" + mem_id + ", comm_ttl=" + comm_ttl + ", comm_type="
				+ comm_type + ", comm_dtl=" + comm_dtl + ", comm_del=" + comm_del + ", comm_hits=" + comm_hits
				+ ", comm_date=" + comm_date + ", comm_like_check=" + ", comm_file=" + comm_file
				+ "]";
	}
	
	
	
}
