package kr.or.ddit.addoption.vo;

public class addoptionVO {
	private String ado_no   ;
	private String camp_no  ;
	private String ado_name ;
	private int ado_price;
	
	
	public String getAdo_no() {
		return ado_no;
	}
	public void setAdo_no(String ado_no) {
		this.ado_no = ado_no;
	}
	public String getCamp_no() {
		return camp_no;
	}
	public void setCamp_no(String camp_no) {
		this.camp_no = camp_no;
	}
	public String getAdo_name() {
		return ado_name;
	}
	public void setAdo_name(String ado_name) {
		this.ado_name = ado_name;
	}
	public int getAdo_price() {
		return ado_price;
	}
	public void setAdo_price(int ado_price) {
		this.ado_price = ado_price;
	}
	
	
	@Override
	public String toString() {
		return "addoptionVO [ado_no=" + ado_no + ", camp_no=" + camp_no + ", ado_name=" + ado_name + ", ado_price="
				+ ado_price + "]";
	}
	
	
	

	
}
