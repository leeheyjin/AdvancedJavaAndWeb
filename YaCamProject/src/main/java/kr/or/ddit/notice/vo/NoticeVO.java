package kr.or.ddit.notice.vo;

public class NoticeVO {
	private int noti_no  ;
	private String noti_ttl ;
	private String noti_dtl ;
	private String noti_date;
	private String noti_file;
	private String mem_id   ;
	private int noti_hit;
	
	public int getNoti_hit() {
		return noti_hit;
	}
	public void setNoti_hit(int noti_hit) {
		this.noti_hit = noti_hit;
	}
	public int getNoti_no() {
		return noti_no;
	}
	public void setNoti_no(int noti_no) {
		this.noti_no = noti_no;
	}
	public String getNoti_ttl() {
		return noti_ttl;
	}
	public void setNoti_ttl(String noti_ttl) {
		this.noti_ttl = noti_ttl;
	}
	public String getNoti_dtl() {
		return noti_dtl;
	}
	public void setNoti_dtl(String noti_dtl) {
		this.noti_dtl = noti_dtl;
	}
	public String getNoti_date() {
		return noti_date;
	}
	public void setNoti_date(String noti_date) {
		this.noti_date = noti_date;
	}
	public String getNoti_file() {
		return noti_file;
	}
	public void setNoti_file(String noti_file) {
		this.noti_file = noti_file;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	
}
