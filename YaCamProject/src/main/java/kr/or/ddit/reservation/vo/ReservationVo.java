package kr.or.ddit.reservation.vo;

public class ReservationVo {
	private int res_no        ;
	private String mem_id        ;
	private String zone_no       ;
	private String camp_no       ;
	private String res_date      ;
	private String res_checkin   ;
	private String res_checkout  ;
	private int res_person    ;
	private int res_car_count ;
	private int res_price     ;
	private String cup_no;
	
	
	public String getCup_no() {
		return cup_no;
	}
	public void setCup_no(String cup_no) {
		this.cup_no = cup_no;
	}
	public int getRes_no() {
		return res_no;
	}
	public void setRes_no(int res_no) {
		this.res_no = res_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getZone_no() {
		return zone_no;
	}
	public void setZone_no(String zone_no) {
		this.zone_no = zone_no;
	}
	public String getCamp_no() {
		return camp_no;
	}
	public void setCamp_no(String camp_no) {
		this.camp_no = camp_no;
	}
	public String getRes_date() {
		return res_date;
	}
	public void setRes_date(String res_date) {
		this.res_date = res_date;
	}
	public String getRes_checkin() {
		return res_checkin;
	}
	public void setRes_checkin(String res_checkin) {
		this.res_checkin = res_checkin;
	}
	public String getRes_checkout() {
		return res_checkout;
	}
	public void setRes_checkout(String res_checkout) {
		this.res_checkout = res_checkout;
	}
	public int getRes_person() {
		return res_person;
	}
	public void setRes_person(int res_person) {
		this.res_person = res_person;
	}
	public int getRes_car_count() {
		return res_car_count;
	}
	public void setRes_car_count(int res_car_count) {
		this.res_car_count = res_car_count;
	}
	public int getRes_price() {
		return res_price;
	}
	public void setRes_price(int res_price) {
		this.res_price = res_price;
	}
	@Override
	public String toString() {
		return "ReservationVo [res_no=" + res_no + ", mem_id=" + mem_id + ", zone_no=" + zone_no + ", camp_no="
				+ camp_no + ", res_date=" + res_date + ", res_checkin=" + res_checkin + ", res_checkout=" + res_checkout
				+ ", res_person=" + res_person + ", res_car_count=" + res_car_count + ", res_price=" + res_price + "]";
	}
	
	
	
	                             
}
