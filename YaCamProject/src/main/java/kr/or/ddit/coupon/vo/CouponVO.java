package kr.or.ddit.coupon.vo;

public class CouponVO {

	private String cup_no    ;     
	private String mem_id    ;     
	private String cup_name  ;     
	private String cup_status;     
	private String cup_sdate ;     
	private String cup_edate ;
	
	
	public String getCup_no() {
		return cup_no;
	}
	public void setCup_no(String cup_no) {
		this.cup_no = cup_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getCup_name() {
		return cup_name;
	}
	public void setCup_name(String cup_name) {
		this.cup_name = cup_name;
	}
	public String getCup_status() {
		return cup_status;
	}
	public void setCup_status(String cup_status) {
		this.cup_status = cup_status;
	}
	public String getCup_sdate() {
		return cup_sdate;
	}
	public void setCup_sdate(String cup_sdate) {
		this.cup_sdate = cup_sdate;
	}
	public String getCup_edate() {
		return cup_edate;
	}
	public void setCup_edate(String cup_edate) {
		this.cup_edate = cup_edate;
	}
	@Override
	public String toString() {
		return "CouponVO [cup_no=" + cup_no + ", mem_id=" + mem_id + ", cup_name=" + cup_name + ", cup_status="
				+ cup_status + ", cup_sdate=" + cup_sdate + ", cup_edate=" + cup_edate + "]";
	}     
	
	
	
	
}
