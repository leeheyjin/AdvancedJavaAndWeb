package chapter02;

public class LongExample {
	public static void main(String[] args) {
		long var1 = 10;
		long var2 = 20l;
//		long var3 = 100000000000;
		long var3 = 100000000000l;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
	}
	/*예상결과*
	 * 10
	 * 20
	 * 100000000000
	 */
}
