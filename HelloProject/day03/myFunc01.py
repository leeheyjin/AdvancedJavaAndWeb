def add(a, b):
    return a + b


def minus(a, b):
    return a - b


def multiple(a, b):
    return a * b


def devide(a, b):
    return a / b


add = add(4, 2)
minus = minus(4, 2)
multiple = multiple(4, 2)
devide = devide(4, 2)

print("add result: ", add)
print("minus result: ", minus)
print("multiple result: ", multiple)
print("devide result: ", devide)
